	@ApiOperation(value = "模糊查询", notes = "根据查询条件模糊查询")
	@RequestMapping(value = "/getListByJsonData", method = RequestMethod.GET, produces = MediaType.APPLICATION_JSON_VALUE)
	public ResultModel getListByJsonData() {
		ResultModel resultModel = new ResultModel();

		String jsonData = request.getParameter(CustomSymbolic.CONTROLLER_PARAM_JSON);
		if (StringUtils.isBlank(jsonData))
			return resultModel;

		Map<String, Object> map = new HashMap<String, Object>();
		StringBuffer selectBuffer = new StringBuffer();
		selectBuffer.append("select A.id id,A.common_field_id commonFieldId,A.process_product processProduct,A.name name,A.process_type_code processTypeCode,A.process_type_text processTypeText,A.crop_type_code cropTypeCode,A.crop_type_text cropTypeText,A.process_strains_code processStrainsCode,A.process_strains_text processStrainsText,A.sale_region_code saleRegionCode,A.sale_region_text saleRegionText,A.sale_area_code saleAreaCode,A.sale_area_text saleAreaText,A.sale_amount saleAmount,A.sale_amount_unit_code saleAmountUnitCode,A.sale_amount_unit_text saleAmountUnitText,A.sale_amount_unit saleAmountUnit,A.sale_volume saleVolume,A.sale_volume_unit_code saleVolumeUnitCode,A.sale_volume_unit_text saleVolumeUnitText,A.sale_volume_unit saleVolumeUnit,A.waste_amount wasteAmount,A.waste_amount_unit_code wasteAmountUnitCode,A.waste_amount_unit_text wasteAmountUnitText,A.waste_amount_unit wasteAmountUnit,A.sale_time saleTime  from da_sale_process A inner join da_sale_process B on A.id=B.id where 1=1 ");
        
        if (!StringUtils.isBlank(jsonData)) {
			JSONObject jsonObject = JSONObject.fromObject(jsonData);

			if (jsonObject.containsKey("selectRows")) {
				JSONArray selectRowsArray = jsonObject.getJSONArray("selectRows");
				if (selectRowsArray.size() > 0) {
					selectBuffer.append(" and A.id in('");
					for (int i = 0; i < selectRowsArray.size(); i++) {
						selectBuffer.append(i == selectRowsArray.size() - 1 ? selectRowsArray.getString(i) + "'" : selectRowsArray.getString(i) + "','");
					}
					selectBuffer.append(")");
				}
			}

			if (jsonObject.containsKey("entityRelated")) {
				JSONObject entityRelatedObject = jsonObject.getJSONObject("entityRelated");
                
				if (entityRelatedObject.containsKey("processProduct") && StringUtils.isNotBlank(entityRelatedObject.getString("processProduct")))
					selectBuffer.append(" and A.process_product like '%").append(entityRelatedObject.getString("processProduct")).append("%'");
				if (entityRelatedObject.containsKey("name") && StringUtils.isNotBlank(entityRelatedObject.getString("name")))
					selectBuffer.append(" and A.name like '%").append(entityRelatedObject.getString("name")).append("%'");
				if (entityRelatedObject.containsKey("processTypeCode") && StringUtils.isNotBlank(entityRelatedObject.getString("processTypeCode")))
					selectBuffer.append(" and A.process_type_code like '%").append(entityRelatedObject.getString("processTypeCode")).append("%'");
			}

			if (jsonObject.containsKey("page")) {
				JSONObject pageObject = jsonObject.getJSONObject("page");
				map.put("Page", pageObject);
			}

			if (jsonObject.containsKey("orderList")) {
				JSONArray orderListArray = jsonObject.getJSONArray("orderList");
				if (orderListArray.size() != 0)
					selectBuffer.append(" order by ");
				for (int i = 0; i < orderListArray.size(); i++) {
					JSONObject orderColumnObject = orderListArray.getJSONObject(i);
					selectBuffer.append("A." + orderColumnObject.getString("columnName"));
					selectBuffer.append(orderColumnObject.getBoolean("isASC") ? " ASC" : " DESC");
					selectBuffer.append((i + 1) == orderListArray.size() ? " " : " ,");
				}
			}
		}

		map.put("Sql", selectBuffer.toString());

		resultModel = daSaleProcessUntBll.getListBySQL(map);

		return resultModel;
	}

